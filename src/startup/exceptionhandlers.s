@******************************************************************************
@ exception_handlers_gcc.s - Exception handlers for GCC
@******************************************************************************

.global UndefInstHandler
.global SVC_Handler
.global AbortHandler
.global IRQHandler
.global FIQHandler

@ Внешние символы из C-кода
.extern interrupt_vectors        @ Таблица векторов прерываний
.extern CPUAbortHandler          @ C-обработчик для Abort

.section .text.handlers, "ax"
.code 32

@******************************************************************************
@ Константы и макросы
@******************************************************************************
.equ MASK_SVC_NUM,      0xFF000000  @ Маска для выделения номера SVC
.equ MODE_SYS,          0x1F        @ Режим System
.equ MODE_IRQ,          0x12        @ Режим IRQ
.equ I_BIT,             0x80        @ Бит маскирования IRQ в CPSR
.equ F_BIT,             0x40        @ Бит маскирования FIQ в CPSR

@ Адреса регистров контроллера прерываний (INTC) для AM335x
@ Эти значения должны совпадать с определениями в hw_intc.h
.equ SOC_AINTC_REGS,    0x48200000  @ Базовый адрес AINTC
.equ INTC_SIR_IRQ,      0x0040      @ Смещение регистра SIR_IRQ
.equ INTC_SIR_FIQ,      0x0044      @ Смещение регистра SIR_FIQ
.equ INTC_CONTROL,      0x0048      @ Смещение регистра CONTROL
.equ INTC_THRESHOLD,    0x0068      @ Смещение регистра THRESHOLD
.equ INTC_IRQ_PRIORITY, 0x0070      @ Смещение регистра IRQ_PRIORITY

@ Значения для регистров INTC
.equ MASK_ACTIVE_IRQ,   0x0000007F  @ Маска номера активного IRQ
.equ NEWIRQAGR,         0x00000001  @ Бит разрешения новых IRQ
.equ NEWFIQAGR,         0x00000002  @ Бит разрешения новых FIQ

@ Вычисление полных адресов регистров
.equ ADDR_SIR_IRQ,      (SOC_AINTC_REGS + INTC_SIR_IRQ)
.equ ADDR_SIR_FIQ,      (SOC_AINTC_REGS + INTC_SIR_FIQ)
.equ ADDR_CONTROL,      (SOC_AINTC_REGS + INTC_CONTROL)
.equ ADDR_THRESHOLD,    (SOC_AINTC_REGS + INTC_THRESHOLD)
.equ ADDR_IRQ_PRIORITY, (SOC_AINTC_REGS + INTC_IRQ_PRIORITY)
@******************************************************************************
@ Undefined Instruction Handler
@******************************************************************************
UndefInstHandler:
    @ Можно добавить отладочный код
    bkpt #0
    b infinite_loop

@ SVC Handler - как в IAR, поддерживает переключение в System режим
SVC_Handler:
    @ Сохраняем контекст в стеке SVC
    STMFD   sp!, {r0-r1, lr}        @ Сохраняем r0, r1 и lr (адрес возврата)
    SUB     sp, sp, #4              @ Резервируем место на стеке для выравнивания

    @ Извлекаем инструкцию SVC, которая вызвала исключение
    LDR     r0, [lr, #-4]           @ lr указывает на следующую инструкцию после SVC
                                    @ lr-4 указывает на саму инструкцию SVC

    @ Выделяем номер SVC (младшие 24 бита инструкции)
    BIC     r0, r0, #MASK_SVC_NUM   @ Очищаем старшие 8 бит, оставляя номер

    @ Проверяем, это номер 458752? (0x70000 - специальный SVC для TI-RTOS)
    CMP     r0, #458752             @ Сравниваем с константой

    @ Если совпадает, переключаемся в System режим при возврате
    MRSEQ   r1, spsr                @ Копируем SPSR (сохраненный статус) в r1
    ORREQ   r1, r1, #MODE_SYS       @ Устанавливаем биты режима System (0x1F)
    MSREQ   spsr_cf, r1             @ Записываем измененный SPSR обратно
                                    @ _cf означает Control Field (биты управления)

    ADD     sp, sp, #4              @ Восстанавливаем указатель стека
    LDMFD   sp!, {r0-r1, pc}^       @ Восстанавливаем регистры и возвращаемся
                                    @ '^' означает копирование SPSR в CPSR

@******************************************************************************
@ SVC Handler (Supervisor Call)
@ Обработчик системных вызовов (ранее SWI)
@ Переключает в System режим при SVC #458752 (используется в TI-RTOS/SYSBIOS)
@******************************************************************************
AbortHandler:
    STMFD   sp!, {r0, lr}           @ Сохраняем регистры

    @ Определяем тип Abort по биту в SPSR
    MRS     r0, spsr                @ Читаем SPSR
    TST     r0, #(1 << 10)          @ Тестируем бит 10 (I/D статус)
                                    @ 0 = Prefetch Abort, 1 = Data Abort
    BNE     DataAbortHandler        @ Если 1, переходим к Data Abort
    BEQ     PrefetchAbortHandler    @ Если 0, переходим к Prefetch Abort

    @ Сюда не должны попасть
    LDMFD   sp!, {r0, pc}           @ Восстанавливаем и возвращаемся


@******************************************************************************
@ IRQ Handler - ПОЛНАЯ ВЕРСИЯ С ПОДДЕРЖКОЙ ВЛОЖЕННЫХ ПРЕРЫВАНИЙ
@
@ Особенности:
@ - Поддержка приоритетов прерываний через INTC
@ - Вложенные прерывания (более высокий приоритет может прервать ISR)
@ - Сохранение/восстановление VFP/NEON контекста
@ - ISR выполняются в System режиме для избежания порчи lr_irq
@ - Немаскируемое прерывание с приоритетом 0 (нельзя прервать)
@******************************************************************************
IRQHandler:
 @ Шаг 1: Корректировка адреса возврата
    @ В режиме IRQ, lr = адрес инструкции + 4, которую нужно выполнить после возврата
    SUB     lr, lr, #4              @ Корректируем lr для правильного возврата

    @ Шаг 2: Сохранение контекста в стеке IRQ
    @ Сохраняем регистры, которые могут быть изменены в ISR
    STMFD   sp!, {r0-r3, r12, lr}   @ Сохраняем r0-r3, r12 (scratch), lr

    @ Шаг 3: Сохранение статусных регистров
    MRS     r12, spsr               @ Сохраняем SPSR_irq (статус прерванной программы)
    VMRS    r1, fpscr               @ Сохраняем FPSCR (VFP статус и флаги)
    STMFD   sp!, {r1, r12}          @ Помещаем оба в стек

    @ Шаг 4: Сохранение VFP/NEON регистров (d0-d7)
    @ Эти регистры используются для float/double и SIMD операций
    VSTMDB  sp!, {d0-d7}            @ Сохраняем D0-D7 (первые 8 double регистров)
                                    @ DB = Decrement Before (уменьшить SP перед записью)

    @ Шаг 5: Работа с контроллером прерываний INTC
    @ Получаем текущий порог прерываний (THRESHOLD)
    LDR     r0, =ADDR_THRESHOLD     @ r0 = адрес регистра THRESHOLD
    LDR     r1, [r0, #0]            @ Читаем текущее значение порога
    STMFD   sp!, {r1}               @ Сохраняем порог в стеке

    @ Получаем приоритет текущего прерывания
    LDR     r2, =ADDR_IRQ_PRIORITY  @ r2 = адрес регистра IRQ_PRIORITY
    LDR     r3, [r2, #0]            @ r3 = приоритет активного IRQ (0-255, меньше=выше)
    STR     r3, [r0, #0]            @ Устанавливаем приоритет как НОВЫЙ порог
                                    @ Это блокирует прерывания с меньшим или равным приоритетом

    @ Получаем номер активного прерывания
    LDR     r1, =ADDR_SIR_IRQ       @ r1 = адрес регистра SIR_IRQ
    LDR     r2, [r1]                @ Читаем регистр
    AND     r2, r2, #MASK_ACTIVE_IRQ @ Выделяем номер прерывания (0-127)

    @ Подтверждаем прерывание в контроллере (если приоритет > 0)
    MOV     r0, #NEWIRQAGR          @ r0 = 1 (бит разрешения новых IRQ)
    LDR     r1, =ADDR_CONTROL       @ r1 = адрес регистра CONTROL
    CMP     r3, #0                  @ Проверяем, не приоритет ли 0
    STRNE   r0, [r1]                @ Если приоритет > 0, пишем NEWIRQAGR
                                    @ Это разрешает генерацию новых IRQ
    DSB                             @ Data Synchronization Barrier
                                    @ Гарантирует завершение записи до продолжения

    @ Шаг 6: Переключение в System режим с разрешенными IRQ
    @ ISR будут выполняться в System режиме, это позволяет:
    @ - Использовать вложенные прерывания без порчи lr_irq
    @ - Вызывать функции с сохранением правильного контекста

    MRS     lr, cpsr                @ Читаем текущий CPSR
    ORR     lr, lr, #MODE_SYS       @ Устанавливаем биты режима System (0x1F)
    CMP     r3, #0                  @ Снова проверяем приоритет
    BICNE   lr, lr, #I_BIT          @ Если приоритет > 0, сбрасываем бит I (разрешаем IRQ)
                                    @ Приоритет 0 - немаскируемый, IRQ остаются запрещены
    MSR     cpsr_cxsf, lr           @ Переключаемся в System режим
                                    @ _cxsf = Control, eXtension, Status, Flags

    @ Шаг 7: Подготовка к вызову ISR
    @ Сохраняем lr_usr (будет испорчен при вызове)
    STMFD   sp!, {lr}               @ Сохраняем lr (теперь это lr_usr)

    @ Загружаем адрес таблицы векторов прерываний
    LDR     r0, =interrupt_vectors  @ r0 = адрес переменной interrupt_vectors
    LDR     r0, [r0]                @ r0 = значение (указатель на массив ISR)

    @ Вычисляем адрес ISR и переходим
    @ interrupt_vectors[r2] где r2 - номер прерывания
    ADD     lr, pc, #0              @ Сохраняем адрес возврата в lr (текущий PC)
    LDR     pc, [r0, r2, lsl #2]    @ Переход по адресу из таблицы векторов
                                    @ lsl #2 = умножение на 4 (размер указателя)

    @ Шаг 8: Возврат из ISR
    @ Восстанавливаем lr_usr
    LDMFD   sp!, {lr}               @ Восстанавливаем lr (lr_usr)

    @ Шаг 9: Переключение обратно в IRQ режим с запрещенными IRQ
    @ Атомарно запрещаем IRQ и переключаемся в IRQ режим
    CPSID   i, #MODE_IRQ            @ CPSID = Change Processor State, Interrupt Disable
                                    @ i = IRQ, #MODE_IRQ = переключиться в режим IRQ

    @ Шаг 10: Восстановление состояния INTC
    LDR     r0, =ADDR_THRESHOLD     @ r0 = адрес регистра THRESHOLD

    @ Если был приоритет 0, нужно подтвердить прерывание сейчас
    LDR     r1, [r0, #0]            @ Читаем текущий порог
    CMP     r1, #0                  @ Проверяем, не 0 ли
    MOVEQ   r2, #NEWIRQAGR          @ Если 0, готовим NEWIRQAGR
    LDREQ   r1, =ADDR_CONTROL       @ Загружаем адрес CONTROL
    STREQ   r2, [r1]                @ Подтверждаем прерывание для приоритета 0

    @ Восстанавливаем сохраненный порог
    LDMFD   sp!, {r1}               @ Достаем сохраненное значение порога
    STR     r1, [r0, #0]            @ Восстанавливаем порог THRESHOLD

    @ Шаг 11: Восстановление контекста в обратном порядке
    @ Восстанавливаем VFP/NEON регистры
    VLDMIA  sp!, {d0-d7}            @ Восстанавливаем D0-D7
                                    @ IA = Increment After (увеличить SP после чтения)

    @ Восстанавливаем статусные регистры
    LDMFD   sp!, {r1, r12}          @ Достаем fpscr (r1) и spsr (r12)
    MSR     spsr_cxsf, r12          @ Восстанавливаем SPSR_irq
    VMSR    fpscr, r1               @ Восстанавливаем FPSCR

    @ Восстанавливаем рабочие регистры и возвращаемся
    LDMFD   sp!, {r0-r3, r12, pc}^  @ Восстанавливаем регистры и возвращаемся
                                    @ '^' означает: копировать SPSR в CPSR
                                    @ и использовать pc из стека для возврата

@******************************************************************************
@ FIQ Handler
@ FIQ (Fast Interrupt Request) - не используется в AM335x
@******************************************************************************
FIQHandler:
    subs pc, lr, #4

@******************************************************************************
@ Prefetch Abort Handler
@ Вызывается при ошибке выборки инструкции
@******************************************************************************
PrefetchAbortHandler:
    @ Сохраняем рабочие регистры и адрес возврата
    STMFD   sp!, {r0-r3, r12, lr}

    @ Сохраняем SPSR_abt для анализа причины ошибки
    MRS     r0, spsr                @ Читаем SPSR режима Abort
    STMFD   sp!, {r0}               @ Сохраняем на стеке

    @ Получаем адрес инструкции, вызвавшей ошибку
    SUB     r0, lr, #4              @ lr в Abort режиме указывает на инструкцию+4

    @ Вызываем C-обработчик (если нужно)
    @ BL     CPU_PrefetchAbortHandler

    @ Восстанавливаем контекст и возвращаемся
    LDMFD   sp!, {r0}               @ Восстанавливаем SPSR
    MSR     spsr_cxsf, r0           @ Записываем обратно в SPSR
    LDMFD   sp!, {r0-r3, r12, pc}^  @ Восстанавливаем и возвращаемся

@******************************************************************************
@ Data Abort Handler
@ Вызывается при ошибке доступа к данным
@******************************************************************************
DataAbortHandler:
    @ Сохраняем рабочие регистры и адрес возврата
    STMFD   sp!, {r0-r3, r12, lr}

    @ Сохраняем SPSR_abt для анализа причины ошибки
    MRS     r0, spsr                @ Читаем SPSR режима Abort
    STMFD   sp!, {r0}               @ Сохраняем на стеке

    @ lr в Data Abort указывает на инструкцию+8, корректируем
    SUB     r0, lr, #8              @ Получаем адрес инструкции, вызвавшей ошибку

    @ Вызываем C-обработчик (если нужно)
    @ BL     CPU_DataAbortHandler

    @ Восстанавливаем контекст и возвращаемся
    LDMFD   sp!, {r0}               @ Восстанавливаем SPSR
    MSR     spsr_cxsf, r0           @ Записываем обратно в SPSR
    LDMFD   sp!, {r0-r3, r12, pc}^  @ Восстанавливаем и возвращаемся

infinite_loop:
    WFI
    b infinite_loop